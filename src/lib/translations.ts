export function getTranslation(language: Language, key: string): string {
  const keys = key.split('.');
  let current: any = translations[language];

  for (const k of keys) {
    if (current && typeof current === 'object') {
      current = current[k];
    } else {
      return key;
    }
  }

  return typeof current === 'string' ? current : key;
}
